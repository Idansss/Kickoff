export function buildMockMessages(rooms: ChatRoom[], currentUserId: string): Record<string, Message[]> {
  const out: Record<string, Message[]> = {}
  const baseTime = now.getTime()
  const sample = (roomId: string, count: number): Message[] =>
    Array.from({ length: count }, (_, i) => ({
      id: `msg-${roomId}-${i}`,
      roomId,
      content: ['What a goal!', 'This ref is blind', 'We need to hold this lead', 'Best match of the season', 'VAR again...'][i % 5],
      authorId: i % 2 === 0 ? currentUserId : 'u2',
      authorName: i % 2 === 0 ? 'Alex Turner' : 'Jordan Silva',
      authorHandle: i % 2 === 0 ? 'alexturner' : 'jordansilva',
      avatarInitials: i % 2 === 0 ? 'AT' : 'JS',
      avatarColor: i % 2 === 0 ? '#16a34a' : '#7c3aed',
      createdAt: new Date(baseTime - (count - i) * 60 * 1000),
    }))
  rooms.forEach((r) => (out[r.id] = sample(r.id, 5)))
  return out
}

